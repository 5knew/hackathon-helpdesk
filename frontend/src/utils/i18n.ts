let currentLanguage: Language = 'ru';

export function setLanguage(lang: Language) {
  currentLanguage = lang;
  localStorage.setItem('app_language', lang);
}

export function updateCurrentLanguage(lang: Language) {
  currentLanguage = lang;
}

export function getLanguage(): Language {
  const saved = localStorage.getItem('app_language') as Language;
  if (saved && ['ru', 'kz', 'en'].includes(saved)) {
    return saved;
  }
  // –û–ø—Ä–µ–¥–µ–ª—è–µ–º —è–∑—ã–∫ –±—Ä–∞—É–∑–µ—Ä–∞
  const browserLang = navigator.language.split('-')[0];
  if (browserLang === 'ru' || browserLang === 'kz' || browserLang === 'en') {
    return browserLang as Language;
  }
  return 'ru';
}

export function t(key: string, lang?: Language): string {
  const langToUse = lang || currentLanguage;
  return translations[langToUse]?.[key] || translations.ru[key] || key;
}

